__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
ARR RN R0
RES_ARR RN R1
	LDR ARR,=SRC
	LDR RES_ARR,=DST
COUNTER RN R3
	MOV COUNTER,#10
LOOP	LDR R4,[ARR],#4
	STR R4,[RES_ARR],#4
	SUBS COUNTER,#1
	BNE LOOP
STOP B STOP
SRC DCD 0X1,0X2,0X3,0X4,0X5,0X6,0X7,0X8,0X9,0XA
	AREA my_data,DATA,READWRITE
DST DCD 0
	END
	